//while Loops
//The while loop is used to repeat a block of
//code as long as a given condition is true.
//For example, let's print "Hello" 5 times:
//Every cycle of a loop is called an iteration.
fun main(args: Array<String>) {
    var k = 1

    while (k <= 5) {
        println("Kude")
        k++
    }
    var x = 4
    while(x > 0) {
        println(x)
        x-=2
    }


}